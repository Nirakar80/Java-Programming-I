
public class Prefix {

	public static void main(String[] args) {

		int x =4; 
		System.out.println("The number is "+ x);
		
		//increment number 
		++x;
		System.out.println("The new number is "+x);
		
		//decrement number
		--x;
		System.out.println("The new number is "+x);
		
		System.out.println("The new number is "+ ++x);

		System.out.println("The new number is "+ ++x);
		

		
	}

}
