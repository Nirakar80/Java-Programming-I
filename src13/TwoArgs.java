
public class TwoArgs 
{

	public static void main(String[] args) 
	{
		double a =8.0 , b =7.9;
		
		showSum(a,b);
		
	}
	
	/*
	  This method is to show the sum of two number.
	*/
	public static void showSum(double a, double b)
	{
		System.out.println("The sum is "+ (a+b));
	}
}
